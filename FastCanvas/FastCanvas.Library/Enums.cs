namespace FastCanvas.Library;

/// <summary>
/// Contains Butt, Round, and Square
/// </summary>
public enum LineCap { Butt, Round, Square }
/// <summary>
/// Contains Miter, Bevel, and Round
/// </summary>
public enum LineJoin { Miter, Bevel, Round }
/// <summary>
/// Contains Left-To-Right, Right-To-Left, and Inherit
/// </summary>
public enum Direction { Ltr, Rtl, Inherit }
/// <summary>
/// Contains Start, End, Center, Left, and Right
/// </summary>
public enum TextAlign { Start, End, Center, Left, Right }
/// <summary>
/// Contains Alphabetic, Top, Hanging, Middle, Ideographic, and Bottom
/// </summary>
public enum TextBaseline { Alphabetic, Top, Hanging, Middle, Ideographic, Bottom }
/// <summary>
/// Contains Repeat, Repeat-x, Repeat-y, and No-Repeat
/// </summary>
public enum Repeat { Repeat, Repeat_X, Repeat_Y, No_Repeat }
